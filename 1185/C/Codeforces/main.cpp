    int sum = 0;
    for (int i = 0; i < n; ++i) {
        cin >> a[i];
        sum += a[i];
        if (sum <= m) {
            ans[i] = 0;
        }
        else {
            sort(a, a + i);
            int tmp = sum;
            while (tmp > m) {
                ++ans[i];
                tmp -= a[i - ans[i]];
            }
        }
    }
    
